    private void initializeSession() {
        if (this.loggedInOperator == null) {
            this.loggedInOperator = LibraryBookBorrowingSystem.loggedInUser;
        }

        if (this.targetPatron == null && (loggedInOperator instanceof Librarian || loggedInOperator instanceof Admin)) {
            promptForTargetPatron();
        } else if (loggedInOperator instanceof Patron) {
            this.targetPatron = (Patron) loggedInOperator;
        }

        if (targetPatron != null) {
            txtPatronInfo.setText(targetPatron.getUsername() + " [ID: " + targetPatron.getUserID() + "]");
            populateBorrowedBooksList();
        } else {
            this.dispose();
            new ManageLoansFrame(loggedInOperator, null).setVisible(true);
        }
    }
    
    private void promptForTargetPatron() {
        String input = JOptionPane.showInputDialog(this, 
                "Enter target Patron Account User ID:", 
                "Librarian Lookup Verification", 
                JOptionPane.QUESTION_MESSAGE);
                
        if (input == null || input.trim().isEmpty()) {
            return;
        }

        try {
            int targetPatronID = Integer.parseInt(input.trim());
            for (User u : LibraryBookBorrowingSystem.userDatabase) {
                if (u instanceof Patron && u.getUserID() == targetPatronID) {
                    this.targetPatron = (Patron) u;
                    break;
                }
            }
            if (this.targetPatron == null) {
                JOptionPane.showMessageDialog(this, 
                        "No registered profile records match User ID: " + targetPatronID, 
                        "System Error", 
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid ID Format. Please enter numeric digits.", "Format Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void populateBorrowedBooksList() {
        DefaultListModel<String> listModel = new DefaultListModel<>();
        if (targetPatron != null) {
            List<Loan> masterLog = targetPatron.getBorrowingHistory().getLoans();
            for (Loan l : masterLog) {
                Book b = l.getBorrowedBook();
                listModel.addElement(b.getTitle() + " [ID: " + b.getBookID() + "] - Due: " + l.getDueDate().toLocalDate());
            }
        }
        lstBorrowedBooks.setModel(listModel);
    }

