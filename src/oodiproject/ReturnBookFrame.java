    private void btnConfirmReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmReturnActionPerformed
        int selectedIndex = lstBorrowedBooks.getSelectedIndex();
        
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "Please select a borrowed book from the listing to return.", "Selection Required", JOptionPane.WARNING_MESSAGE);
            return;
        }

        List<Loan> activeLoans = targetPatron.getBorrowingHistory().getLoans();
        Loan selectedLoan = activeLoans.get(selectedIndex);
        Book borrowedBook = selectedLoan.getBorrowedBook();
        String selectedCondition = (String) cmbCondition.getSelectedItem();

        borrowedBook.updateCondition(selectedCondition);
        borrowedBook.updateStatus(true);

        double transactionFine = selectedLoan.calculateFine();
        
        if ("Lost".equalsIgnoreCase(selectedCondition)) {
            transactionFine += 50.00; 
        }

        if (transactionFine > 0) {
            targetPatron.setCurrentFees(targetPatron.getCurrentFees() + transactionFine);
            JOptionPane.showMessageDialog(this, 
                "[FEES ASSESSED]: An automated tracking fine of RM" + String.format("%.2f", transactionFine) + 
                "\nhas been assessed and compiled into this profile's outstanding bills.", 
                "Account Fees Modification Notification", 
                JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, 
                "[RETURN SUCCESSFUL]: Book returned on schedule in satisfactory condition.\nNo system penalties incurred.", 
                "Transaction Confirmed", 
                JOptionPane.INFORMATION_MESSAGE);
        }

        activeLoans.remove(selectedIndex);

        LibraryBookBorrowingSystem.saveData();

        populateBorrowedBooksList();
    }//GEN-LAST:event_btnConfirmReturnActionPerformed

