    private void loadCurrentConfigurations() {
        SystemSettings settings = LibraryBookBorrowingSystem.globalSettings;
        
        txtMaxLoanLimit.setText(String.valueOf(settings.getMaxLoanLimit()));
        txtMaxRenewals.setText(String.valueOf(settings.getMaxRenewals()));
        txtFineThreshold.setText(String.format("%.2f", settings.getFineThreshold()));
        txtMaxRenewalDuration.setText(String.valueOf(settings.getMaxRenewalDuration()));
        txtFineRate.setText(String.format("%.2f", settings.getFineRate()));
        
        resetFieldBackgrounds();
    }
    
    private void resetFieldBackgrounds() {
        Color defaultWhite = Color.WHITE;
        txtMaxLoanLimit.setBackground(defaultWhite);
        txtMaxRenewals.setBackground(defaultWhite);
        txtFineThreshold.setBackground(defaultWhite);
        txtMaxRenewalDuration.setBackground(defaultWhite);
        txtFineRate.setBackground(defaultWhite);
    }
    
    private void highlightInvalidFields() {
        Color errorRed = new Color(255, 204, 204);
        
        try { 
            if (Integer.parseInt(txtMaxLoanLimit.getText().trim()) < 0) throw new NumberFormatException(); 
        } catch (NumberFormatException e) { txtMaxLoanLimit.setBackground(errorRed); }
        
        try { 
            if (Integer.parseInt(txtMaxRenewals.getText().trim()) < 0) throw new NumberFormatException(); 
        } catch (NumberFormatException e) { txtMaxRenewals.setBackground(errorRed); }
        
        try { 
            if (Double.parseDouble(txtFineThreshold.getText().trim()) < 0) throw new NumberFormatException(); 
        } catch (NumberFormatException e) { txtFineThreshold.setBackground(errorRed); }
        
        try { 
            if (Integer.parseInt(txtMaxRenewalDuration.getText().trim()) < 0) throw new NumberFormatException(); 
        } catch (NumberFormatException e) { txtMaxRenewalDuration.setBackground(errorRed); }
        
        try { 
            if (Double.parseDouble(txtFineRate.getText().trim()) < 0) throw new NumberFormatException(); 
        } catch (NumberFormatException e) { txtFineRate.setBackground(errorRed); }
    }

