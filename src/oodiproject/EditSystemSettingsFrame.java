    private void saveChangesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveChangesBtnActionPerformed
        resetFieldBackgrounds();
        
        try {
            int maxLoanLimit = Integer.parseInt(txtMaxLoanLimit.getText().trim());
            int maxRenewals = Integer.parseInt(txtMaxRenewals.getText().trim());
            double fineThreshold = Double.parseDouble(txtFineThreshold.getText().trim());
            int maxRenewalDuration = Integer.parseInt(txtMaxRenewalDuration.getText().trim());
            double fineRate = Double.parseDouble(txtFineRate.getText().trim());
            
            if (maxLoanLimit < 0 || maxRenewals < 0 || fineThreshold < 0 || maxRenewalDuration < 0 || fineRate < 0) {
                throw new NumberFormatException("Negative constraints violated");
            }
            
            SystemSettings settings = LibraryBookBorrowingSystem.globalSettings;
            settings.setMaxLoanLimit(maxLoanLimit);
            settings.setMaxRenewals(maxRenewals);
            settings.setFineThreshold(fineThreshold);
            settings.setMaxRenewalDuration(maxRenewalDuration);
            settings.setFineRate(fineRate);
            
            LibraryBookBorrowingSystem.saveData();
            
            JOptionPane.showMessageDialog(this, 
                    "Configuration Changes Successful!\nConfiguration Successfully Updated.", 
                    "Success", JOptionPane.INFORMATION_MESSAGE);
            
        } catch (NumberFormatException e) {
            highlightInvalidFields();
            JOptionPane.showMessageDialog(this, 
                    "Error: Invalid system configurations detected.\nPlease ensure all forms contain positive numeric formats.", 
                    "Invalid Input", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveChangesBtnActionPerformed

