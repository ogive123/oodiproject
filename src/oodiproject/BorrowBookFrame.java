    private void configureUserAccess() {
        if (loggedInOperator instanceof Patron) {
            targetPatron = (Patron) loggedInOperator;
        
            txtPatronID.setVisible(false);
            lblPatronPrompt.setVisible(false);
        
        } else if (loggedInOperator instanceof Librarian) {
            txtPatronID.setVisible(true);
            lblPatronPrompt.setVisible(true);
            txtPatronID.setEditable(true); 
        }
    }
    
    private void populateBookList() {
        DefaultListModel<String> listModel = new DefaultListModel<>();
        displayedBooks.clear();

        ArrayList<Book> catalog = LibraryBookBorrowingSystem.globalCatalog.getBooks();

        for (Book b : catalog) {
            String availability = b.isIsAvailable() ? "Available" : "Checked Out";
            listModel.addElement(b.getTitle() + " [ID: " + b.getBookID() + "] — (" + availability + ")");
            displayedBooks.add(b);
        }

        lstBooks.setModel(listModel);
    }
    
