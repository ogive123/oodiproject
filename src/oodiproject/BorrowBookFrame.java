    private void btnBorrowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrowActionPerformed
        if (loggedInOperator instanceof Librarian || loggedInOperator instanceof Admin) {
            try {
                int enteredID = Integer.parseInt(txtPatronID.getText().trim());
                Patron found = null;
                
                for (User u : LibraryBookBorrowingSystem.userDatabase) {
                    if (u instanceof Patron && u.getUserID() == enteredID) {
                        found = (Patron) u;
                        break;
                    }
                }
                
                if (found == null) {
                    JOptionPane.showMessageDialog(this, "Patron account matching ID " + enteredID + " could not be found.", "Invalid Account", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                targetPatron = found;
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Please enter a valid numeric Patron ID.", "Input Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        int selectedIdx = lstBooks.getSelectedIndex();
        if (selectedIdx == -1) {
            JOptionPane.showMessageDialog(this, "Please choose a book from the list above first.", "No Selection", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Book selectedBook = displayedBooks.get(selectedIdx);

        if (!selectedBook.isIsAvailable()) {
            JOptionPane.showMessageDialog(this, "Book Unavailable! \"" + selectedBook.getTitle() + "\" is already loaned out.", "Book Unavailable [E1]", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!(loggedInOperator instanceof Librarian)) {
            
            double outstandingBalance = targetPatron.getCurrentFees() + targetPatron.getBorrowingHistory().calculateUnpaidFines();
            if (outstandingBalance >= LibraryBookBorrowingSystem.globalSettings.getFineThreshold()) {
                JOptionPane.showMessageDialog(this, 
                    "Sorry, you may not perform this action until your fines are cleared.\n" +
                    "Your Balance: RM" + String.format("%.2f", outstandingBalance), 
                    "Action Blocked [E2]", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int activeLoansCount = 0;
            for (Loan l : targetPatron.getBorrowingHistory().getLoans()) {
                if ("Due".equalsIgnoreCase(l.getLoanStatus()) || "Overdue".equalsIgnoreCase(l.getLoanStatus())) {
                    activeLoansCount++;
                }
            }
            if (activeLoansCount >= LibraryBookBorrowingSystem.globalSettings.getMaxLoanLimit()) {
                JOptionPane.showMessageDialog(this, "Loan Limit Exceeded! You have already checked out your maximum allowed limit of " + activeLoansCount + " books.", "Quota Limit Hit [E5]", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        selectedBook.setIsAvailable(false);
        Loan newLoanItem = new Loan(selectedBook);
        targetPatron.getBorrowingHistory().addLoanRecord(newLoanItem);
        
        LibraryBookBorrowingSystem.saveData();

        JOptionPane.showMessageDialog(this, "Successfully checked out \"" + selectedBook.getTitle() + "\" for " + targetPatron.getUsername() + "!", "Transaction Approved", JOptionPane.INFORMATION_MESSAGE);
        
        populateBookList();
    }//GEN-LAST:event_btnBorrowActionPerformed

    
